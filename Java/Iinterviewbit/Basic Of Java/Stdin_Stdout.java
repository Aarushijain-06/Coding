import java.util.*;

public class Stdin_Stdout {
    public static void main(String[] args) {
        
        Scanner s = new Scanner(System.in);
        int a = s.nextInt();
        int b = s.nextInt();
       
        System.out.println(a);      // printing the output of a 
        System.out.println(b);      // printing the output of b 
    }  
}